import numpy_financial as npf

# آرایه جریان نقدی (به میلیون تومان)
# +20  → پول دریافتی در ماه ۰
#  0   → ماه اول تنفس (هیچ پرداختی نیست)
#  0   → ماه دوم تنفس
# -8  → قسط اول
# -7  → قسط دوم
# -6  → قسط سوم

cashflows = [20_000_000, 0, 0, -8_000_000, -7_000_000, -6_000_000]

# محاسبه IRR ماهانه
monthly_rate = npf.irr(cashflows)

apr = monthly_rate * 12 * 100          # نرخ اسمی سالانه
ear = ((1 + monthly_rate)**12 - 1) * 100   # نرخ موثر سالانه

print(f"نرخ سود ماهانه (IRR)     : {monthly_rate:.6f}  ({monthly_rate*100:.4f}٪)")
print(f"نرخ اسمی سالانه (APR)     : {apr:.2f}٪")
print(f"نرخ موثر سالانه (EAR)     : {ear:.2f}٪")
print(f"جمع اقساط پرداخت شده      : ۲۱ میلیون تومان")
print(f"سود کل                     : ۱ میلیون تومان")